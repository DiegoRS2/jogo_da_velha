    public void verificaJogador(){
           if(jogadorUm == (true) && jogadorDois == true){
               this.controlaJogador = 1;
               jogadorUm = false;
               jogadorDois = true;
               txtVisor.setText("Vez do jogador O");
           }
           else if(jogadorUm == true && jogadorDois == false ){
               this.controlaJogador = 1;
               jogadorUm = false;
               jogadorDois = true;
               txtVisor.setText("Vez do jogador O");
           }
           else{
             this.controlaJogador = 2;
             jogadorUm = true;
             jogadorDois = false;
             txtVisor.setText("Vez do jogador X");     
           }
    }
    
